// การประกาศตัวแปรใน javascript 

var test = "test"; //  var จะมีขอบเขตเป็นระดับ function var สามารถใช้ซ้ำได้และสามารถเปลี่ยนแปลงค่าได้

let test1 = 1;  // let จะมีขอบเขตเป็น block scope ซึ่งหมายความว่าตัวแปรจะมองเห็นได้ภายใน block นั้นๆ ตัวแปรที่ประกาศด้วย let สามารถใช้ซ้ำได้สามารถเปลี่ยนแปลงค่าได้

const test2 = 2;  // const จะมีขอบเขตเป็น block scope ซึ่งหมายความว่าตัวแปรจะมองเห็นได้ภายใน block นั้นๆ ตัวแปรที่ประกาศด้วย const สามารถใช้ซ้ำได้แต่ไม่สามารถเปลี่ยนแปลงค่าได้

